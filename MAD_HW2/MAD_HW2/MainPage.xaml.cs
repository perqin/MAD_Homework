namespace MAD_HW2
{
    /// <summary>
    /// An empty page that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class MainPage : Page
    {
        public MainPage()
        {
            this.InitializeComponent();
        }

        protected override void OnNavigatedTo(NavigationEventArgs e)
        {
            Frame rootFrame = Window.Current.Content as Frame;

            if (rootFrame.CanGoBack)
            {
                // Show UI in title bar if opted-in and in-app backstack is not empty.
                SystemNavigationManager.GetForCurrentView().AppViewBackButtonVisibility = AppViewBackButtonVisibility.Visible;
            }
            else
            {
                // Remove the UI from the title bar if in-app back stack is empty.
                SystemNavigationManager.GetForCurrentView().AppViewBackButtonVisibility = AppViewBackButtonVisibility.Collapsed;
            }
        }

        private void AddButton_Click(object sender, RoutedEventArgs e)
        {
            this.Frame.Navigate(typeof(EditPage));
        }

        private void TodoDone0_Click(object sender, RoutedEventArgs e)
        {
            TodoTitleDelLine0.Visibility = TodoDone0.IsChecked == true ? Visibility.Visible : Visibility.Collapsed;
        }

        private void TodoDone1_Click(object sender, RoutedEventArgs e)
        {
            TodoTitleDelLine1.Visibility = TodoDone1.IsChecked == true ? Visibility.Visible : Visibility.Collapsed;
        }
    }
}
